/**
 * Description of CampaignInputFilter
 *
 * @author gabriel
 */
class CampaignInputFilter extends InputFilter {

    public function __construct()
    {
        $this->add([
            'name' => 'name',
            'required' => true,
            'filters' => [
                ['name' => StringTrim::class],
                ['name' => StripTags::class]
            ],
            'validators' => [
                [
                    'name' => NotEmpty::class,
                    'break_chain_on_failure' => true,
                    'options' => [
                        'messages' => [
                            NotEmpty::IS_EMPTY => "Este campo é requerido"
                        ]
                    ]
                ],
            ]
        ]);
        
        $this->add([
            'name' => 'subject',
            'required' => true,
            'filters' => [
                ['name' => StringTrim::class],
                ['name' => StripTags::class]
            ],
            'validators' => [
                [
                    'name' => NotEmpty::class,
                    'break_chain_on_failure' => true,
                    'options' => [
                        'messages' => [
                            NotEmpty::IS_EMPTY => "Este campo é requerido"
                        ]
                    ]
                ],
            ]
        ]);
        
        $this->add([
            'name' => 'tags',
            'required' => true,
            'filters' => [
                ['name' => StringTrim::class],
                ['name' => StripTags::class]
            ],
            'validators' => [
                [
                    'name' => NotEmpty::class,
                    'break_chain_on_failure' => true,
                    'options' => [
                        'messages' => [
                            NotEmpty::IS_EMPTY => "Este campo é requerido"
                        ]
                    ]
                ],
            ]
        ]);
        
        $this->add([
            'name' => 'template',
            'required' => true,
            'validators' => [
                [
                    'name' => NotEmpty::class,
                    'break_chain_on_failure' => true,
                    'options' => [
                        'messages' => [
                            NotEmpty::IS_EMPTY => "Este campo é requerido"
                        ]
                    ]
                ],
            ]
        ]);
    }

}
